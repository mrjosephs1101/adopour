import { consumeStream, convertToModelMessages, streamText, type UIMessage } from "ai"

export const maxDuration = 30

export async function POST(req: Request) {
  const { messages }: { messages: UIMessage[] } = await req.json()

  const prompt = convertToModelMessages(messages)

  const result = streamText({
    model: "openai/gpt-5-mini",
    system: `You are AdoAI, a friendly and helpful AI assistant for Adopour, a social media platform. 
    You help users with questions about the platform, provide social media tips, and engage in friendly conversation.
    You have a cheerful, water-themed personality (matching the Adopour water droplet logo).
    Keep responses concise and helpful.`,
    prompt,
    abortSignal: req.signal,
  })

  return result.toUIMessageStreamResponse({
    onFinish: async ({ isAborted }) => {
      if (isAborted) {
        console.log("[v0] Chat aborted")
      }
    },
    consumeSseStream: consumeStream,
  })
}
